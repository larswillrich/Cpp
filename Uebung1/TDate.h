#ifndef TDATE_H_
#define TDATE_H_
#include <string>

class TDate {
public:
	TDate();
	TDate(int day, int month, int year);
	virtual ~TDate();

    int day;
    int month;
    int year;

    int getDay() const
    {
        return day;
    }

    void setDay(int day)
    {
        this->day = day;
    }

    int getMonth() const
    {
        return month;
    }

    void setMonth(int month)
    {
        this->month = month;
    }

    int getYear() const
    {
        return year;
    }

    void setYear(int year)
    {
        this->year = year;
    }
};

#endif /* TDATE_H_ */
